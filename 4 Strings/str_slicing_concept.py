# STRING SLICING CONCEPT

print("\nQ1. Python Programing")
#    01234567890123456   +
s = "Python programing"
#    76543210987654321   -

print(s[3:13])
print(s[1:15:3])
print(s[:14])
print(s[5:])
print(s[::5])
print(s[10])

print(s[-15:-2])
print(s[-17:-7:2])
print(s[:-1])
print(s[-14:])
print(s[::-2])


