# What are the types of functions :
# There are two types of functions in python
# 1.User-Defined Functions - these types of functions are defined by the user to perform any specific task
# 2.Built-in Functions - These are pre-defined functions in python.


# 1.User-Defines Function 
def sub(x, y):
    return x-y
print(sub(5,2))


# 2.Built-in Function
l = [2, 5, 19, 7, 43]
print("Length of string is",len(l))
print("Maximum number in list is ",max(l))
print("Type is",type(l))